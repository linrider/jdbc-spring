CREATE TABLE person (
	id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	name VARCHAR(30),
	age INT
);

CREATE TABLE car (
	id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	producer VARCHAR(30),
	model VARCHAR(30),
	manufacture_year INT,
	origin VARCHAR(30)
);

CREATE TABLE users (
	id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	name VARCHAR(50) NOT NULL,
	username VARCHAR(50) NOT NULL UNIQUE,
	age INT NOT NULL,
	email VARCHAR(50) NOT NULL UNIQUE,
	address VARCHAR(100) NOT NULL
);
